# Faça um programa para escrever a contagem regressiva do lançamento de um foguete.
# O programa deve imprimir 10, 9, 8, ..., 1, 0 e Fogo! na tela.

x = 10 # Contador inicia em 10.

while x >= 0: # Laço de repetição while.
    print(x) # Exibe no terminal o valor do contador.
    x = x - 1 # Subtrai 1 do contador.

# Quando o laço de repetição passa ser falso, a mensagem "E fogo!" é exibida no terminal.

print("e Fogo!")