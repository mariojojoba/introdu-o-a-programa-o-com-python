# Reescreva o programa anterior para escrever os 10 primeiros múltiplos de 3.

contador = 1 # Contador

while contador <= 10: # Laço de repetição
    múltiplo = contador * 3 # Crio a variável múltiplo, que multiplica o contador por três.
    print (múltiplo) # Exibo no terminal a váriavel múltiplo.
    contador = contador + 1 # Somo mais um ao contador.
