# No comando print, você só precisa colocar "aspas" se você estiver se referindo a uma "string".

print(10 + 20 * 30) # o "*" se refere ao sinal de multiplicação.
print(4**2 / 30) # os "**" se referem a potenciação, no caso, seria 4².
print((9**4 + 2) * 6 - 1) # os parênteses organizam a estrutura da expressão.